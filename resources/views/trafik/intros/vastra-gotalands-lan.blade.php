<p>
    Västra Götaland är Sveriges näst folkrikaste län och rymmer både
    Sveriges andra storstad och stora delar av rikets västkust- och
    inlandstrafik. E6 längs västkusten, E20 mot Stockholm, E45 norrut
    längs Göta älv och Riksväg 40 mot Jönköping är de mest belastade
    statliga vägarna. Tingstadstunneln och Tingstadsmotet på E6 i
    norra Göteborg är en av Sveriges mest trafikerade
    motorvägsknutpunkter och en återkommande källa till
    incidentrapporter när köer eller olyckor uppstår.
</p>

<p>
    Trafikhändelser i länet domineras i normalfall av tre kategorier:
    pågående vägarbeten kopplade till Västlänken och övriga
    infrastrukturprojekt i Göteborgsregionen, vägstörningar på E6 och
    E20 — där olyckor får stor regional effekt eftersom alternativa
    rutter är få — samt landsvägsolyckor i Skaraborg och Bohuslän,
    där vägnätet är glesare och hastigheterna högre. Färjelägen kring
    Tjörn, Orust och Stenungsund kan generera störningar men filtreras
    bort från denna lista. Vinterväglag förekommer i inlandet
    (Skaraborg, Dalsland) medan kusten oftast har problem först kring
    nollgraderskift med halka.
</p>

<p>
    Sidan kombinerar två källor: live-händelser från Trafikverkets
    öppna data (olyckor, vägarbeten, hinder, trafikmeddelanden på
    statliga vägar i länet) och polisrapporterade händelser från
    Polisens RSS (trafikolyckor, trafikkontroller, trafikbrott).
    Filtret <em>Visa bara olyckor</em> begränsar listan till
    olycks-klassade händelser.
</p>
