<p>
    E6 längs västkusten, E20 mot Stockholm, E45 norrut längs Göta älv och
    Riksväg 40 mot Jönköping är de mest belastade statliga vägarna i länet.
    Tingstadstunneln och Tingstadsmotet på E6 i norra Göteborg är en av
    Sveriges mest trafikerade motorvägsknutpunkter och en återkommande källa
    till incidenter när köer eller olyckor uppstår. Trafikbilden domineras
    normalt av pågående vägarbeten kring Västlänken och övriga
    infrastrukturprojekt i Göteborgsregionen, störningar på E6 och E20 där
    olyckor får stor regional effekt eftersom alternativa rutter är få, samt
    landsvägsolyckor i Skaraborg och Bohuslän där vägnätet är glesare och
    hastigheterna högre. Vinterväglag förekommer i inlandet medan kusten
    oftast bara har halka kring nollgraderskift. Färjelägen kring Tjörn,
    Orust och Stenungsund kan generera störningar men filtreras bort från
    listan. Sidan uppdateras löpande i takt med respektive källa.
</p>
