<p>
    Stockholms län är Sveriges mest trafikerade vägnät. E4 genom länet —
    inklusive Essingeleden — har dygnsmedel kring 160&nbsp;000 fordon
    och är landets enskilt mest belastade vägavsnitt. Tillsammans med
    E18, E20, Södra länken, Norra länken och Värmdöleden står de stora
    statliga vägarna för merparten av de incidenter Trafikverket
    rapporterar i realtid i länet.
</p>

<p>
    Trafikhändelser kring Stockholm domineras i normalfall av tre
    kategorier: planerade vägarbeten kopplade till de pågående
    infrastrukturprojekten (Förbifart Stockholm, Mälarbanan, Tvärbanans
    utbyggnad, Slussen), korta avstängningar och köbildningar vid
    olyckor i Essingeledens tunnlar och på Södra länken, samt
    omledningar under nattetid när tyngre arbeten utförs för att inte
    störa pendlingstoppen. Halka kring nollgrader och plötsliga
    snöbyar är vanligaste vinterstörning — vinterväglag i större
    omfattning är ovanligt jämfört med övre Norrland.
</p>

<p>
    Sidan kombinerar två källor: live-händelser från Trafikverkets
    öppna data (olyckor, vägarbeten, hinder, trafikmeddelanden — alla
    statliga vägar i länet) och polisrapporterade händelser från
    Polisens RSS (trafikolyckor, trafikkontroller, trafikbrott).
    Trafikverket rapporterar bredare, Polisen rapporterar selektivt
    men ger blåljus-perspektivet. Filtret <em>Visa bara olyckor</em>
    nedan begränsar listan till händelser klassade som olycka i
    respektive källa.
</p>
