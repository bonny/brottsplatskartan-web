<p>
    E4 genom länet — inklusive Essingeleden — har dygnsmedel kring
    160&nbsp;000 fordon och är landets enskilt mest belastade vägavsnitt.
    Tillsammans med E18, E20, Södra länken, Norra länken och Värmdöleden
    står de stora statliga vägarna för merparten av de incidenter
    Trafikverket rapporterar. Trafikbilden domineras normalt av planerade
    vägarbeten kring pågående infrastrukturprojekt (Förbifart Stockholm,
    Mälarbanan, Tvärbanans utbyggnad, Slussen), korta avstängningar och
    köbildningar vid olyckor i Essingeledens tunnlar och på Södra länken,
    samt nattetida omledningar för att inte störa pendlingstoppen. Halka
    kring nollgrader och plötsliga snöbyar är de vanligaste vinterstörningarna —
    vinterväglag i större omfattning är ovanligt jämfört med övre Norrland.
    Under pendlingstoppen morgon och eftermiddag fortplantar sig även mindre
    incidenter snabbt till stora köer eftersom kapaciteten ligger nära taket.
    Listan nedan uppdateras löpande i takt med att respektive källa rapporterar.
</p>
