<p>
    Skåne har Sveriges tätaste vägnät per kvadratkilometer och en
    trafikprofil som skiljer sig från övriga landet. E6 längs
    västkusten från Helsingborg via Malmö ned mot Trelleborg, E22 från
    Malmö genom Lund och Kristianstad mot Blekinge, och E4 som ändar i
    Helsingborg är länets tre stora pulsådror. Öresundsbron och dess
    påfartsleder kring Malmö är dessutom en internationell
    transportkorridor mot Köpenhamn och kontinenten, vilket gör
    incidenter där märkbara även för pendlartrafiken.
</p>

<p>
    Trafikhändelser i länet domineras normalt av tre kategorier:
    olyckor och störningar i Malmö-regionen där pendlings- och
    färjegodstrafik möts, viltolyckor på landsvägsavsnitten kring
    Söderåsen och de inre delarna — Skåne har en av landets högsta
    viltolycksfrekvenser räknat per vägkilometer — samt
    väderrelaterade störningar från Öresund: dimma, kraftig sidvind
    över Öresundsbron och plötsliga regnskurar. Vinterväglag i
    klassisk mening är ovanligt; halka kring nollgraderskift och
    plötsliga snöbyar är de typiska vinterstörningarna.
</p>

<p>
    Sidan kombinerar två källor: live-händelser från Trafikverkets
    öppna data (olyckor, vägarbeten, hinder, trafikmeddelanden på
    statliga vägar i länet) och polisrapporterade händelser från
    Polisens RSS (trafikolyckor, trafikkontroller, trafikbrott).
    Filtret <em>Visa bara olyckor</em> begränsar listan till
    olycks-klassade händelser.
</p>
