</p>

<h2>Cookies</h2>

<p>
    Cookies (även kallade för "kakor" på svenska) är små filer som skickas till din webbläsare och sparas av din
    webbläsare.
    De används för att lagra olika typer av information.</p>

<h2>Cookies på Brottsplatskartan</h2>

<p>Vi använder cookies för att mäta trafiken på vår webbplats, för statistik och för att anpassa annonser.</p>

<p>
    Vi använder Google AdSense för att visa annonser. 
    När du besöker vår webbplats placerar Google eller dess annonspartners en cookie i din webbläsare.
</p>

<h2>Geografisk plats</h2>

<p>
    Om du använder vår funktion <a href="/nara-hitta-plats">"nära dig"</a> så 
    frågar vi din webbläsare om vi får hämta din geografiska position
    och om du godkänner detta så används denna information till att visa 
    händelser från Polisen som hänt nära dig. 
</p>
<p>
    Vi sparar inte denna information och besöker du mig-sidan igen så kommer vi ännu en
    gång fråga din webbläsare om denna information. 
    Beroende på webbläsare så får du en fråga igen eller så minns ditt tidigare val.
</p>
